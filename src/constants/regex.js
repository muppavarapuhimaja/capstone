export const EMAIL_VALIDATION_REGEX = RegExp(
    /^(([^<>()[\].,;:\s@"]+(\.[^<>()[\].,;:\s@"]+)*)|(".+"))@(([^<>()[\].,;:\s@"]+\.)+[^<>()[\].,;:\s@"]{2,})$/i
);

export const PASSWORD_VALIDATION_REGEX = RegExp(
    /^(?=.*[a-z])(?=.*[A-Z])(?=.*[0-9])(?=.*[!@#$%^&*])(?=.{8,})/
);

export const ALPHABETS_VALIDATION_REGEX = RegExp(
    /^[a-zA-Z]+$/
);
